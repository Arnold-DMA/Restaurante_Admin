    private void actualizar(){
        modelo=new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nombre");
        modelo.addColumn("Precio");
        modelo.addColumn("Descripción");
        modelo.addColumn("Categoría");
        modelo.addColumn("Estado");
        
        
        this.jTable1.setModel(modelo);
        this.setLocationRelativeTo(null);
        try{
            
            PreparedStatement ps = null;
            ResultSet rs = null;
            
            Conectar conn = new Conectar();
            Connection con = conn.getConnection();
            
            String sql = "SELECT * FROM COMIDA";
            
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            ResultSetMetaData rsMd = rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
         
            
            while(rs.next()){
                Object[] filas = new Object[cantidadColumnas];
                for(int i = 0; i<cantidadColumnas; i++){
                    if(i!=4){
                        filas[i] = rs.getObject(i + 1);
                    }
                    else{
                        ResultSet rs2 = null;
                        sql = "SELECT TIPNOM FROM TIPO WHERE TIPCOD = ?";
                        ps = con.prepareStatement(sql);
                        ps.setString(1, rs.getObject(i+1).toString());
                        rs2 = ps.executeQuery();
                        rs2.next();
                        filas[i] = rs2.getObject(1);
                    }
                }
                modelo.addRow(filas);
                
                //jtEmpresaTransportista.setPreferredSize(new Dimension(240, jtEmpresaTransportista.getPreferredSize().height + 16));
            }
            
            conn.desconectar();
        }
        catch(SQLException ex){
            System.err.println(ex.toString());
        }
        llenarComboBox();
    }

