    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try{
            PreparedStatement ps = null;
            Conectar conn = new Conectar();
            Connection con = conn.getConnection();
            ResultSet rs = null;
            String sql;
            boolean existe = false;
            sql = "SELECT * FROM COMIDA WHERE COMCOD = ?";
            ps = con.prepareStatement(sql);
            ps.setString(1, txtCodigo.getText());
            rs = ps.executeQuery();
            while(rs.next())
                existe = true;
            
            String estado;
            if(checkEstado.isSelected()){
                estado = "Activo";
            }
            else{
                estado = "Inactivo";
            }
            //****************************
            ResultSet rs2 = null;
            PreparedStatement ps2 = null;
            sql = "SELECT TIPCOD FROM TIPO WHERE TIPNOM = ?";
            ps2 = con.prepareStatement(sql);
            ps2.setString(1, comboCategoria.getSelectedItem().toString());
            rs2 = ps2.executeQuery();
            rs2.next();
            String tipo = rs2.getObject(1).toString();
            //****************************
            if(existe){
                sql = "UPDATE COMIDA SET COMNOM = ?, COMPRE = ?, COMDES = ?, COMTIPCOD = ?, COMEST = ? WHERE COMCOD = ?";
                ps = con.prepareStatement(sql);
                ps.setString(1, txtNombre.getText());
                ps.setString(2, txtPrecio.getText());
                ps.setString(3, txtDescripcion.getText());
                ps.setString(4, tipo);
                ps.setString(5, estado);
                ps.setString(6, txtCodigo.getText());
            }
            else{
                sql = "INSERT INTO COMIDA (COMCOD, COMNOM, COMPRE, COMDES, COMTIPCOD, COMEST) VALUES (?, ?, ?, ?, ?, ?)";
                ps = con.prepareStatement(sql);
                ps.setString(1, txtCodigo.getText());
                ps.setString(2, txtNombre.getText());
                ps.setString(3, txtPrecio.getText());
                ps.setString(4, txtDescripcion.getText());
                ps.setString(5, tipo);
                ps.setString(6, estado);
            }
            ps.executeUpdate();
            actualizar();
        }
        catch(SQLException ex){
            System.err.println(ex.toString());
        }
        txtCodigo.setText("");
        txtNombre.setText("");
        txtPrecio.setText("");
        txtDescripcion.setText("");
        checkEstado.setSelected(false);
        
    }//GEN-LAST:event_jButton2ActionPerformed

